    private void obtenerResultadoVendedor() {
        Object[] datos = new Object[4];

        String codEmpleado = cbVededores.getItemAt(cbVededores.getSelectedIndex()).getCodEmpleado();
        Date fechaInicio = jcInicio.getDate();
        long inicio = fechaInicio.getTime();
        Date fechaFIn = jcFin.getDate();
        long fin = fechaFIn.getTime();

        java.sql.Date fecha_inicio = new java.sql.Date(inicio);
        java.sql.Date fecha_fin = new java.sql.Date(fin);

        for (RatioVendedorDTO ratio : oRatioVendor.listar(codEmpleado, fecha_inicio.toString(), fecha_fin.toString())) {
            datos[0] = ratio.getCodEmpleado();
            datos[1] = ratio.getVendedor();
            datos[2] = ratio.getTotalVisitas();
            datos[3] = ratio.getVentasRealizadas();
            modeloRatio.addRow(datos);
        }
        tbRatioVendedor.setModel(modeloRatio);
    }

    private void calcularRatio() {
        DecimalFormat formato = new DecimalFormat("#.##");
        int totalVisitas = Integer.parseInt(modeloRatio.getValueAt(0, 2).toString());
        int ventasRealizadas = Integer.parseInt(modeloRatio.getValueAt(0, 3).toString());
        double ratioEfectividad = ((double) ventasRealizadas / totalVisitas) * 100;

        jLabel7.setText(formato.format(ratioEfectividad)+ " %");

        if (ratioEfectividad >= 50) {
            jLabel8.setText("El empleado cumple con eficacia en la generaciÃ³n de ventas a partir de las interacciones con los clientes");
        } else {
            jLabel8.setText("Se recomienda mejorar su capacidad para cerrar ventas durante las visitas");
        }
    }
